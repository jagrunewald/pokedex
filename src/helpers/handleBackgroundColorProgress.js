const handleBackgroundColorProgress = (type) => {
  if(type === 'grass') {
    return 'gray';
  }
  if(type === 'fighting') {
    return 'gray';
  }
  if(type === 'dark') {
    return 'gray';
  }
  if(type === 'dragon') {
    return 'gray';
  }
  if(type === 'fairy') {
    return 'gray';
  }
  if(type === 'rock') {
    return 'gray';
  }
  if(type === 'poison') {
    return 'gray';
  }
  else {
    return 'white';
  }
}

export default handleBackgroundColorProgress;
